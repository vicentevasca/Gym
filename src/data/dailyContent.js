/**
 * Devuelve el contenido del día actual.
 * @returns {{ verso, concepto, microReto }}
 */
export function getDailyContent() {
  const now        = new Date()
  const dayOfMonth = now.getDate() - 1   // 0–30
  const dayOfWeek  = now.getDay()        // 0–6

  return {
    verso:     VERSOS[Math.min(dayOfMonth, VERSOS.length - 1)],
    concepto:  CONCEPTOS[dayOfWeek],
    microReto: MICRO_RETOS[Math.min(dayOfMonth, MICRO_RETOS.length - 1)],
  }
}
